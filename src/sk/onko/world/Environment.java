public class Environment {

    //RGB values of environment color (0-255)
    private int R = 100;
    private int G = 205;
    private int B = 250;

    public int getR() {
        return R;
    }

    public void setR(int r) {
        R = r;
    }

    public int getG() {
        return G;
    }

    public void setG(int g) {
        G = g;
    }

    public int getB() {
        return B;
    }

    public void setB(int b) {
        B = b;
    }
}
